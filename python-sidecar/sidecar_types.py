class EmailContact(TypedDict, total=False):
    name: str
    email: str
    company: str


EmailItem = TypedDict("EmailItem", {
    "id": str,
    "thread_id": str,
    "subject": str,
    "from": str,
    "to": str,
    "date": str,
    "body": str,
    "contact": "EmailContact",
}, total=False)
EmailItem.__doc__ = """Canonical email shape returned by all readers (IMAP, Gmail, file)."""


# ---------------------------------------------------------------------------
# Graph data  (matches the /graph API response contract)
# ---------------------------------------------------------------------------

class GraphEdgeEvidence(TypedDict, total=False):
    """Evidence backing a single directed edge in the graph."""
    quote: str
    date: str


GraphEdge = TypedDict("GraphEdge", {
    "from": str,
    "to": str,
    "type": str,
    "weight": float,
    "color": str,
    "evidence": list["GraphEdgeEvidence"],
}, total=False)
GraphEdge.__doc__ = """Directed edge in the relationship graph.

    Note: This matches the wire format that ``build_graph()`` returns
    (``from`` / ``to``).  The internal storage format in HydraDB uses
    ``from_person`` / ``to_person`` — the conversion happens in
    ``relationship_graph.store_relations()``.
    """


class GraphNode(TypedDict, total=False):
    id: str
    label: str
    importance: float
    type: str


class GraphData(TypedDict, total=False):
    nodes: list[GraphNode]
    edges: list[GraphEdge]


class DeviceInfo(TypedDict):
    index: int
    name: str
    channels: int
    default_samplerate: float


